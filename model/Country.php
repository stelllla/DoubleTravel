	public function __construct($data = NULL)
	{
		if (!is_array($data)) {
			return;
		}

		foreach (get_object_vars($this) as $name => $v) {
			if (!array_key_exists($name, $data)) {
				continue;
			}
			$v = $data[$name];
			if ($name == 'currencies') {
				$v = implode(',', $v);
			}
			if ($name == 'languages') {
				$fullNames = [];
				for ($i = 0; $i < count($v); $i++) {
					$fullNames[$i] = $this->languageMap[$v[$i]];
				}
				$v = implode(',', $fullNames);
			}
			$this->$name = $v;
		}
	}

	public function toArray($prefix = '')
	{
		$ret = array();
		foreach (get_object_vars($this) as $name => $v) {
			if (method_exists($v, 'toArray')) {
				$v = $v->toArray($prefix);
			}
			$ret[$prefix . $name] = $v;
		}
		return $ret;
	}

